namespace Sandbox.Core {
    /// <summary>
    /// Supported types of display elements.
    /// </summary>
    public enum DisplayElement {
        /// <summary>
        /// Display as a checkbox.
        /// </summary>
        Checkbox,
        /// <summary>
        /// Display as a textbox.
        /// </summary>
        Textbox,
        /// <summary>
        /// Display as a rich textbox.
        /// </summary>
        RichTextbox,
        /// <summary>
        /// Display as a drop down list.
        /// </summary>
        DropdownList
    }
}
